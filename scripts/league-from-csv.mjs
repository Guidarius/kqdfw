#!/usr/bin/env node
// Regenerates lib/league-days.ts from a spreadsheet CSV export.
//
// Usage:
//   node scripts/league-from-csv.mjs schedule.csv
//
// Expected CSV columns (header row required, any order):
//   date,day_name,venue,time,cab,blue,gold,score
//
// Example rows:
//   2026-06-14,Opening Day,Free Play Dallas,4:00 PM,Cab 1,Bear Minimum,Hive Five,
//   2026-06-14,Opening Day,Free Play Dallas,4:20 PM,Cab 1,Show Me The Honey,Pollen Count,3-1
//
// "cab" and "score" may be left empty. Score format: blue-gold, e.g. 3-1.

import { readFileSync, writeFileSync } from "node:fs";

const csvPath = process.argv[2];
if (!csvPath) {
  console.error("usage: node scripts/league-from-csv.mjs schedule.csv");
  process.exit(1);
}

const lines = readFileSync(csvPath, "utf8").trim().split(/\r?\n/);
const header = lines
  .shift()
  .split(",")
  .map((s) => s.trim().toLowerCase());
const col = (name) => {
  const i = header.indexOf(name);
  if (i === -1) {
    console.error(`CSV is missing required column: ${name}`);
    process.exit(1);
  }
  return i;
};

const iDate = col("date");
const iName = col("day_name");
const iVenue = col("venue");
const iTime = col("time");
const iCab = header.indexOf("cab");
const iBlue = col("blue");
const iGold = col("gold");
const iScore = header.indexOf("score");

const days = new Map();
for (const line of lines) {
  if (!line.trim()) continue;
  const cells = line.split(",").map((s) => s.trim());
  const date = cells[iDate];
  if (!/^\d{4}-\d{2}-\d{2}$/.test(date)) {
    console.error(`Bad date "${date}" — use ISO format like 2026-06-14`);
    process.exit(1);
  }
  if (!days.has(date)) {
    days.set(date, {
      date,
      name: cells[iName],
      venue: cells[iVenue],
      matches: [],
    });
  }
  const match = {
    time: cells[iTime],
    ...(iCab !== -1 && cells[iCab] ? { cab: cells[iCab] } : {}),
    blue: cells[iBlue],
    gold: cells[iGold],
  };
  const score = iScore !== -1 ? cells[iScore] : "";
  if (score) {
    const parts = score.split("-").map(Number);
    if (parts.length !== 2 || parts.some(Number.isNaN)) {
      console.error(`Bad score "${score}" — use blue-gold like 3-1`);
      process.exit(1);
    }
    match.score = parts;
  }
  days.get(date).matches.push(match);
}

const sorted = [...days.values()].sort((a, b) => a.date.localeCompare(b.date));

const out = `// GENERATED by scripts/league-from-csv.mjs — do not edit by hand.
// Re-export the spreadsheet as CSV and re-run the script instead.

import type { LeagueDay } from "./league";

export const leagueDays: LeagueDay[] = ${JSON.stringify(sorted, null, 2)};
`;

writeFileSync(new URL("../lib/league-days.ts", import.meta.url), out);
console.log(
  `Wrote lib/league-days.ts: ${sorted.length} league days, ${[...days.values()].reduce((n, d) => n + d.matches.length, 0)} matches`
);
